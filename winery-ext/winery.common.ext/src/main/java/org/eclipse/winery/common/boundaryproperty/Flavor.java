package org.eclipse.winery.common.boundaryproperty;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class Flavor {
    @XmlAttribute
    private String name;
    @XmlAttribute
    private String desc;
    
    @XmlElementWrapper(name = "Items")
    @XmlElement(name = "Item")
    private List<FlavorConfigItem> flavorItem;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public List<FlavorConfigItem> getFlavorItem() {
        return flavorItem;
    }

    public void setFlavorItem(List<FlavorConfigItem> flavorItem) {
        this.flavorItem = flavorItem;
    }
}
